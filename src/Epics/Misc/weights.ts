world.afterEvents.entitySpawn.subscribe(({ entity }) => {
    if (!entity || !entity.isValid) return;

    const id = entity.typeId;
    if (!id.startsWith("pokeworld:")) return;

    // ⏳ Delay until tags & components are applied
    system.runTimeout(() => {
        if (!entity.isValid) return;

        const isWild = entity.hasTag("wild");
        const isSummoned = entity.hasTag("summoned");

        if (!isWild && !isSummoned) return;

        // ✅ Resolve Pokémon name
        const rawName = isWild
            ? id.slice("pokeworld:wild_".length)
            : id.slice("pokeworld:".length);

        const lookupName =
            rawName.toLowerCase() === "mr.mime"
                ? "mr_mime"
                : rawName.toLowerCase();

        const weight = pokemonWeights[lookupName];
        if (weight === undefined) return;

        try {
            const objective = world.scoreboard.getObjective("weight");
            if (!objective) return;

            // ✅ Do nothing if already set
            const existing = objective.getScore(entity);
            if (existing !== undefined) return;

            entity.runCommand(`scoreboard players set @s weight ${weight}`);
        } catch {
            /* silent */
        }
    }, 2); // 2 ticks is safe
});
